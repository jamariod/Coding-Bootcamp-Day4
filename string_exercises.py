#Uppercase a String
new_word = input("Enter any word you want changed to upper case: ")
print(new_word.upper())

#Capitalize a String
lower_case_word = input("Enter any text all lower case in you want capitalize the first letter: ")
print(lower_case_word.capitalize())

#Reverse a String

your_word = input("Enter any word you want to reverse: ")
print(your_word[::-1])





